    private void clear() {
        tfProductName.setText(null);
        tfPrice.setText(null);
    }

    private void tableProduct() {
        dao.getallProducts(tbManageProduct);
        model = (DefaultTableModel) tbManageProduct.getModel();
        tbManageProduct.setRowHeight(100);
        tbManageProduct.setShowGrid(true);
        tbManageProduct.setGridColor(Color.black);
        tbManageProduct.setBackground(Color.white);
        tbManageProduct.setSelectionBackground(Color.gray);
        tbManageProduct.setModel(model);
        tbManageProduct.getTableHeader().setReorderingAllowed(false);
        tbManageProduct.getColumnModel().getColumn(3).setCellRenderer(new ManageProductsFrame.ImageRenderer());

    }

    private class ImageRenderer extends DefaultTableCellRenderer {

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            JLabel jL = new JLabel();
            byte[] bytes = (byte[]) value;
            ImageIcon imageicon = new ImageIcon(new ImageIcon(bytes).getImage().getScaledInstance(100, 100, Image.SCALE_DEFAULT));
            jL.setIcon(imageicon);
            return jL;
        }

    }

