    private void tbManageProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbManageProductMouseClicked
        model = (DefaultTableModel) tbManageProduct.getModel();
        rowIndex = tbManageProduct.getSelectedRow();
        tfProductName.setText(model.getValueAt(rowIndex, 1).toString());
        tfPrice.setText(model.getValueAt(rowIndex, 2).toString());
    }//GEN-LAST:event_tbManageProductMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (tfProductName.getText().isEmpty() && tfPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a product", "Warning", 2);
        } else {

            try {
                Product product = new Product();
                product.setId(Integer.parseInt(model.getValueAt(rowIndex, 0).toString()));
                product.setName(tfProductName.getText().trim());
                product.setPrice(Double.parseDouble(tfPrice.getText().trim()));
                if (dao.update(product)) {
                    JOptionPane.showMessageDialog(this, "Product updated");
                    tbManageProduct.setModel(new DefaultTableModel(null, new Object[]{"ID", "Name", "Price", "Image"}));
                    dao.getallProducts(tbManageProduct);
                    tbManageProduct.getTableHeader().setReorderingAllowed(false);
                    tbManageProduct.getColumnModel().getColumn(3).setCellRenderer(new ManageProductsFrame.ImageRenderer());
                    clear();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "" + e, "Warning", 2);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

